// Функция для автоматического выбора эмодзи на основе названия категории
export function getEmojiForCategory(categoryName: string): string {
  const lowerName = categoryName.toLowerCase().trim();
  
  // Прямое совпадение
  if (emojiMap[lowerName]) {
    return emojiMap[lowerName];
  }
  
  // Поиск по ключевым словам
  for (const [keyword, emoji] of Object.entries(emojiMap)) {
    if (lowerName.includes(keyword)) {
      return emoji;
    }
  }
  
  // Если ничего не найдено, возвращаем дефолтный эмодзи
  return '🍽️';
}

